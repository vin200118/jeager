package com.core.cat;

public class TraceData {


	String traceId;
	public TraceData() {
		
	}
	public TraceData(String traceId) {
		super();
		this.traceId = traceId;
	}
	public String getTraceId() {
		return traceId;
	}

	public void setTraceId(String traceId) {
		this.traceId = traceId;
	}

	
	

}
